package com.microfocus.keystrokedynamics.pages;

public class KeyParam {
	private String keyCode;
	private double keyup;
	private double keydown;
	private double held;
	public String getKeyCode() {
		return keyCode;
	}
	public void setKeyCode(String keyCode) {
		this.keyCode = keyCode;
	}
	public double getKeyup() {
		return keyup;
	}
	public void setKeyup(double keyup) {
		this.keyup = keyup;
	}
	public double getKeydown() {
		return keydown;
	}
	public void setKeydown(double keydown) {
		this.keydown = keydown;
	}
	public double getHeld() {
		return held;
	}
	public void setHeld(double held) {
		this.held = held;
	}
	
	

}
